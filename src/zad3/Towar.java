package zad3;

public class Towar {

	private String id;
	private int waga;

	public Towar(String id, int waga) {
		this.id = id;
		this.waga = waga;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public int getWaga() {
		return waga;
	}

	public void setWaga(int waga) {
		this.waga = waga;
	}

}
